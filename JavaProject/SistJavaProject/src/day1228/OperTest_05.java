package day1228;

public class OperTest_05 {

	public static void main(String[] args) {
		int a, b, c, d, e;
		a = b = c = d = e = 10;
		
		a += 2;
		b -= 3;
		c *= 3;
		d /= 3;
		e %= 3;
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		System.out.println("d = " + d);
		System.out.println("e = " + e);

	}

}
