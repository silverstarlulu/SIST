package day0106;

public class MyDate_02 {
	private int year;
	private int month;
	private int day;
	
	public MyDate_02(int y,int m,int d) {
		year=y;
		month=m;
		day=d;
	}
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
}
